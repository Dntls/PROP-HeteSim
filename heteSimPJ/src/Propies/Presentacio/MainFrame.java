    private void afgbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_afgbuttonActionPerformed
        // TODO add your handling code here:
        int result = 0;
        NewEditController nec = new NewEditController(hc);
        String nom = nomafeg.getText();
        if(tipoafg.getSelectedItem().toString()== "Autor"){
            ArrayList<String> papers = make_array("p");
            result = nec.addAuthor(nom, papers);
            mostrar_result(result);
        }
        if(tipoafg.getSelectedItem().toString() == "Paper"){
            ArrayList<String> terms = make_array("t");
            String conf = confAfg.getText();
            ArrayList<String> autors = make_array("a");
            result = nec.addPaper(nom,autors,conf,terms);
            mostrar_result(result);

        }
        if(tipoafg.getSelectedItem().toString() == "Conferenia"){
            ArrayList<String> papers = make_array("p");
            result = nec.addConf(nom, papers);
            mostrar_result(result);
        }
        if(tipoafg.getSelectedItem().toString() == "Term"){
            ArrayList<String> papers = make_array("p");
            result = nec.addTerm(nom, papers);
            mostrar_result(result);
        }
        if (result == 0){
            nec.confirmChange();
            iessucks = true;
        }
    }//GEN-LAST:event_afgbuttonActionPerformed

