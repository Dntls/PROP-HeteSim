    private ArrayList<String> make_array(String type){
        String a = "";
        if(type == "p"){
            a= papersAfg.getText();
        }else if (type == "a"){ a = autorsAfg.getText();}
        else if(type == "t"){ a = termsAfg.getText();}
        else if (type == "f") a = filt.getText();
        ArrayList<String> list = new ArrayList();
        String[] parts = a.split("\\r?\\n");
        int size = parts.length;
        for(int i = 0;i<size;++i){
            //if(parts[i].length() > 110) len = true;  
            list.add(parts[i]);
        }
        return list;
    }

